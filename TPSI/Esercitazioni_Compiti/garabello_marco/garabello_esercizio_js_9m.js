let add = (a,b) => a+b;
let sub = (a,b) => a-b;
let mol = (a,b) => a*b;
let div = (a,b) => a/b;


console.log("Addizioni:");
console.log(add(3,2));
console.log(add(6,3));

console.log("Sottrazioni:");
console.log(sub(5,2));
console.log(sub(3,1));

console.log("Moltiplicazioni:");
console.log(mol(5,3));
console.log(mol(2,20));

console.log("Divisioni:");
console.log(div(10,2));
console.log(div(30,3));
